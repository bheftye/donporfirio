	function validar_campos(){
        var myRegExp =/^(?:(?:https?|ftp):\/\/)(?:\S+(?::\S*)?@)?(?:(?!10(?:\.\d{1,3}){3})(?!127(?:\.\d{1,3}){3})(?!169\.254(?:\.\d{1,3}){2})(?!192\.168(?:\.\d{1,3}){2})(?!172\.(?:1[6-9]|2\d|3[0-1])(?:\.\d{1,3}){2})(?:[1-9]\d?|1\d\d|2[01]\d|22[0-3])(?:\.(?:1?\d{1,2}|2[0-4]\d|25[0-5])){2}(?:\.(?:[1-9]\d?|1\d\d|2[0-4]\d|25[0-4]))|(?:(?:[a-z\u00a1-\uffff0-9]+-?)*[a-z\u00a1-\uffff0-9]+)(?:\.(?:[a-z\u00a1-\uffff0-9]+-?)*[a-z\u00a1-\uffff0-9]+)*(?:\.(?:[a-z\u00a1-\uffff]{2,})))(?::\d{2,5})?(?:\/[^\s]*)?$/i;

		if(form1.link_video.value != ""){
            if (form1.link_video.value.length < 2 || !myRegExp.test(form1.link_video.value)){
           form1.link_video.focus();
           $('#link').removeClass("form-group").addClass("form-group has-error");
           $('.top-right').notify({
               message: { text: 'Esta no es una link válida (Ej. de link válida https://www.youtube.com/watch?v=_zR6ROjoOX0&spfreload=10)' },
               type:'blackgloss',
             }).show();
           return false;
           }
          else{
           $('#link').removeClass("form-group has-error").addClass("form-group has-success");
          }
        }
        else{
            return false;
        }
		
	}
	$(document).ready(function () {
	    $(".buttonguardar").click(function () {
	        if (validar_campos()!= false){
	        	$("#myModal").modal("show");
	        }
	    });
	});
	</script>


